<p align="justify" class="maincontent"style="margin-top:0px">&nbsp; &nbsp; &nbsp; &nbsp;துன்புறு சூழ்நிலைகளில் இருக்கும் குழந்தைகள் மற்றும் பெண்களுடைய உரிமைகளை உறுதிசெய்வதிலும், அவர்களுக்கு பாதுகாப்பு மற்றும் பராமரிப்பு வழங்குவதில் தமிழ்நாடு முதல் நிலை வகித்துவருகிறது. குழந்தை பராமரிப்பு மற்றும் பாதுகாப்பினை உறுதி செய்யும் வகையில் “மெட்ராஸ் குழந்தைகள் சட்டம்” என்னும் பிரத்தியேக சட்டத்தினை 1920-ம் ஆண்டில் இந்தியாவிலேயே முதலில் மெட்ராஸ் ராஜதானியில் இயற்றப்பட்டது. துன்பம் மற்றும் தார்மீக ஆபத்துகளில் இருக்கும் சிறுமியர் மற்றும் பெண்களுக்கு ஏதுவான மறுவாழ்வு சேவைகள், அதனுடன் மகிழ்ச்சி நிறைந்த மற்றும் அக்கறை மிகுந்த சூழலை வழங்கும் முனைப்புடன் சமூக பாதுகாப்புத் துறையானது செயல்படுகிறது. சமூக பாதுகாப்புத் துறையானது, இளஞ்சிறார் நீதிச் (குழந்தைகள் பாதுகாப்பு மற்றும் பராமரிப்பு) சட்டம் 2015, ஒழுக்க நெறி பிறழ்தல் (தடுப்பு) சட்டம் 1956 ஆகியவற்றை செயல்படுத்துதல் மற்றும் பாலியல் குற்றத்திலிருந்து குழந்தையை பாதுகாக்கும் சட்டம் 2012-ன் கீழ் வரக்கூடிய பாதிப்புக் குள்ளானவர்களுக்கு உரிய மறுவாழ்வு நடவடிக்கை எடுத்தல் ஆகியப்பணிகளை ஆற்றி வருகின்றது.</p>
<p align="justify" class="maincontent">&nbsp; &nbsp; &nbsp; &nbsp;  தமிழக அரசின் சமூக நலம் மற்றும் சத்துணவுத் திட்டத் துறையின் கீழ் சமூக பாதுகாப்புத் துறையானது இயக்குநர், சமூக பாதுகாப்புத் துறை அவர்களின் தலைமையில் இயங்கிவருகின்றது.</p> 
