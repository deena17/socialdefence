<p align="justify" class="maincontent" id="content" style="margin-top:0px">&nbsp; &nbsp; &nbsp; &nbsp; The State of Tamil Nadu stands first in providing care for the Children and Women in difficult circumstances and protection of their rights. The Madras Children Act, 1920 was enacted for the care and protection of children by which Tamil Nadu has become the first State in India to enact an exclusive legislation for children. The Department of Social Defence is committed to provide caring and joyful atmosphere with appropriate rehabilitation to the girls and women in distrees and moral danger. The Department of Social Defence is implementing three important legislations in the State, namely the Juvenile Justice (Care and Protection of Children) Act, 2000, the immoral Traffic (Prevention) Act, 1956 and Protection of children from Sexual Offences Act, 2012.</p>
<p align="justify" class="maincontent" id="content">&nbsp; &nbsp; &nbsp; &nbsp; The Department of Social Defence is functioning under the Department of Social Welfare and Nutritious Meal Programme of the State Government. The Department is headed by the Director of Social Defence.</p> 
